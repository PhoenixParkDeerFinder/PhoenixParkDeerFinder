CREATE OR REPLACE FUNCTION "public"."generate_username"() RETURNS "text"
    LANGUAGE "plpgsql"
    AS $$declare
  adjectives text[] := array[
    'swift','bold','quiet','brave','calm','eager','fierce',
    'gentle','keen','nimble','russet','amber','silver','misty','wild'
  ];
  animals text[] := array[
    'deer','fox','hawk','wolf','bear','owl','lynx',
    'hare','elk','boar','robin','raven','badger','otter','stoat'
  ];
  candidate text;
  attempts  int := 0;
begin
  loop
    candidate := adjectives[1 + floor(random() * array_length(adjectives, 1))::int]
              || '_'
              || animals[1 + floor(random() * array_length(animals, 1))::int]
              || '_'
              || (floor(random() * 900) + 100)::int;

    -- Ensure uniqueness (very unlikely to collide but safe)
    exit when not exists (select 1 from public.profiles where username = candidate);
    attempts := attempts + 1;
    if attempts > 10 then
      candidate := candidate || attempts::text;
      exit;
    end if;
  end loop;
  return candidate;
end;$$;


ALTER FUNCTION "public"."generate_username"() OWNER TO "postgres";


CREATE OR REPLACE FUNCTION "public"."handle_new_user"() RETURNS "trigger"
    LANGUAGE "plpgsql" SECURITY DEFINER
    AS $$begin
  insert into public.profiles (id, username)
  values (new.id, public.generate_username());
  return new;
end;$$;


